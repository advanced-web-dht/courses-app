import React, { memo } from 'react';
import type { NextPage } from 'next';
import { GetServerSideProps } from 'next';
import Head from 'next/head';
import Button from '@mui/material/IconButton';

import ClassIcon from '@mui/icons-material/Class';
import PlusIcon from '@mui/icons-material/Add';

import Classes from '../src/components/classes';
import Header from '../src/components/header';
import AddClassModal from '../src/components/addClassModal';
import useToggleModal from '../src/components/addClassModal/hook';
import ClassProvider from '../src/store/class';
import { GetAllClasses } from '../src/api';

import { IClass } from '../src/type';

interface ClassesPageProps {
	classes: IClass[];
}

const addClassModalWithButton = () => {
	const { isOpen, handleOpen, handleClose } = useToggleModal();

	return (
		<React.Fragment>
			<Button onClick={handleOpen}>
				<PlusIcon />
			</Button>
			<AddClassModal open={isOpen} handleClose={handleClose} />
		</React.Fragment>
	);
};

const AddClassModalWithButton = memo(addClassModalWithButton);

const Home: NextPage<ClassesPageProps> = ({ classes }: ClassesPageProps) => {
	return (
		<ClassProvider initClasses={classes}>
			<Head>
				<title>Classes</title>
				<meta name='description' content='Generated by create next app' />
				<link rel='icon' href='/favicon.ico' />
			</Head>
			<Header title='Fit classes' icon={<ClassIcon />} rightAction={<AddClassModalWithButton />} />
			<Classes />
		</ClassProvider>
	);
};

export const getServerSideProps: GetServerSideProps = async () => {
	const classes: IClass[] = await GetAllClasses();
	return {
		props: {
			classes
		}
	};
};

export default Home;
